//Program to print 5 times "Jay Ganesh...! " on screen

class Assignment1_Q2{

    public static void PrintG(int No){
       
        for(int i=0;i<No;i++){

            System.out.println(" Jay Ganesh...! ");
        }
    }

    public static void main(String[] args) {
       
        PrintG(5); 
    }
}